#include "libft.h"

int		ft_lstsize(t_list *ls)
{
	int		i;
	t_list	*it;

	i = 0;
	it = ls;
	if (ft_lstcycle(ls))
		return (-1);
	while (it != NULL)
	{
		i++;
		it = it->next;
	}
	return (i);
}
